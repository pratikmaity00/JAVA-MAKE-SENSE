public class Grade {
    public static void main(String[] args) {
        double mark = 80;

        if(mark>=90) 
            System.out.println("A");
        else if(mark>=75)
            System.out.println("B");
        else if(mark>=60)
            System.out.println("C");
        else
            System.out.println("Fail");
    }
}
