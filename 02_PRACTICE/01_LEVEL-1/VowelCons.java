public class VowelCons {
    public static void main(String[] args) {
        char alpahbet = 'Z';

        if(alpahbet == 'a'  || alpahbet == 'e' || alpahbet == 'i' || alpahbet == 'o' || alpahbet == 'u' || alpahbet == 'A'  || alpahbet == 'E' || alpahbet == 'I' || alpahbet == 'O' || alpahbet == 'U')
            System.out.println(alpahbet + " is a Vowel.");
        else
            System.out.println(alpahbet + " is a Consonant.");
    }
}
