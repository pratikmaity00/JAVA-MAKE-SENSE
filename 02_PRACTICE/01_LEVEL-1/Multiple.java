public class Multiple {
    public static void main(String[] args) {
        int num = 21;
        int multiple = 3*7;

        if(num%multiple == 0)
            System.out.println(num + " is multiple of both 3 and 7.");
        else
            System.out.println(num + " is not multiple of both 3 and 7.");
    }
}
